public class Endereco {
    private String rua;
    private int numero;

    // Construtor
    public Endereco(String rua, int numero) {
        this.rua = rua;
        this.numero = numero;
    }

    // Getters
    public String getRua() {
        return rua;
    }

    public int getNumero() {
        return numero;
    }
}
