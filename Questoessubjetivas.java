//2) O que acontece se uma classe for programada como final? E se
//um atributo for “setado” como final?
//RESPOSTA:
//Se uma classe for programada como final, ela não pode ser estendida (herdada) por outras classes.
//Se um atributo for "setado" como final, ele não pode ser modificado após sua inicialização, ou seja, 
//seu valor permanece constante durante a execução do programa

