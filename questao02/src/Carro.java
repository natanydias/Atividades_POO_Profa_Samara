public class Carro {
    // Atributos privados
    private String marca;
    private int ano;

    // Construtor
    public Carro(String marca, int ano) {
        this.marca = marca;
        this.ano = ano;
    }

    // Getters
    public String getMarca() {
        return marca;
    }

    public int getAno() {
        return ano;
    }
}
